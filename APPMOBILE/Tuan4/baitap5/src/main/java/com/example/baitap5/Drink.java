package com.example.baitap5;

public class Drink {
    int imageDrink;
    String nameDrink;

    public Drink(int imageDrink, String nameDrink) {
        super();
        this.imageDrink = imageDrink;
        this.nameDrink = nameDrink;
    }

    public int getImageDrink() {
        return imageDrink;
    }

    public void setImageDrink(int imageDrink) {
        this.imageDrink = imageDrink;
    }

    public String getNameDrink() {
        return nameDrink;
    }

    public void setNameDrink(String nameDrink) {
        this.nameDrink = nameDrink;
    }
}
